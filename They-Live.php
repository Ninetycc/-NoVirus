<p>Walter John Kilner was born on May 23, 1847, at Bury St. Edmunds in Suffolk, England. 
Kilner became interested in the human aura, a luminous area surrounding all living things 
and visible to sensitives. Baron Georg von Reichenbach (1772–1826), a German astronomical 
instrument maker, claimed to be able to see auras around the poles of magnets 
and around human hands. This intrigued Kilner and in 1908, he began experimenting to see 
if he could produce a screen which, if looked through, 
would make the aura visible to the non-sensitive eye.</p> 

<p>By 1911, he had devised a system using a dilute solution of a dye called dicyanin, a product of coal tar. 
Another method he tried initially was looking first at a bright light through a strong alcoholic solution 
and then looking at the subject. This, however, proved to be very dangerous, causing damage to the eyes. 
Kilner eventually perfected his dicyanin method and produced what became known as the Kilner Screen. 
He published his findings in The Human Atmosphere (1911). In his method, 
researchers looked at a subject through the screen, in daylight, 
with the dicyanin contained in two pieces of hermetically sealed glass.</p>  
<p>The subject was a naked person standing against a dark background. 
In this way, three distinct radiations could be seen. 
The first was dark and colorless and extended from the body no more than a fraction of an inch. 
The second extended beyond the first about three inches. The last was at least a foot in length. 
The first aura Kilner termed the etheric double; the second, the inner aura; and the last, 
the outer aura. Illness was seen to affect the color and size of the aura, 
with mental deterioration causing reduction in size and impending death shrinking the aura to almost nothing.

Kilner’s book was the first to study the human aura as a scientific fact, rather than as a questionable psychic phenomenon.

